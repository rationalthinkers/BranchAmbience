package com.branch.data;

public class BigFive {

	private int opennessInsight;
	private int conscientiousnessInsight;
	private int extraversionInsight;
	private int agreeablenessInsight;
	private int emotionalRangeInsight;
	private String colour;
	private String musicType;

	public String getMusicType() {
		return musicType;
	}

	public void setMusicType(String musicType) {
		this.musicType = musicType;
	}

	public String getColour() {
		return colour;
	}

	public void setColour(String colour) {
		this.colour = colour;
	}

	public int getOpennessInsight() {
		return opennessInsight;
	}

	public void setOpennessInsight(int opennessInsight) {
		this.opennessInsight = opennessInsight;
	}

	public int getConscientiousnessInsight() {
		return conscientiousnessInsight;
	}

	public void setConscientiousnessInsight(int conscientiousnessInsight) {
		this.conscientiousnessInsight = conscientiousnessInsight;
	}

	public int getExtraversionInsight() {
		return extraversionInsight;
	}

	public void setExtraversionInsight(int extraversionInsight) {
		this.extraversionInsight = extraversionInsight;
	}

	public int getAgreeablenessInsight() {
		return agreeablenessInsight;
	}

	public void setAgreeablenessInsight(int agreeablenessInsight) {
		this.agreeablenessInsight = agreeablenessInsight;
	}

	public int getEmotionalRangeInsight() {
		return emotionalRangeInsight;
	}

	public void setEmotionalRangeInsight(int emotionalRangeInsight) {
		this.emotionalRangeInsight = emotionalRangeInsight;
	}

}
