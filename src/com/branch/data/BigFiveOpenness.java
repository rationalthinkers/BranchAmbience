package com.branch.data;

public class BigFiveOpenness {

	private int adventurousnessPercent;
	private int artisticInterestsPercent;
	private int emotionalityPercent;
	private int imaginationPercent;
	private int intellectPercent;
	private int authorityChallengingPercent;
	private String colour;

	public String getColour() {
		return colour;
	}

	public void setColour(String colour) {
		this.colour = colour;
	}

	public int getAdventurousnessPercent() {
		return adventurousnessPercent;
	}

	public void setAdventurousnessPercent(int adventurousnessPercent) {
		this.adventurousnessPercent = adventurousnessPercent;
	}

	public int getArtisticInterestsPercent() {
		return artisticInterestsPercent;
	}

	public void setArtisticInterestsPercent(int artisticInterestsPercent) {
		this.artisticInterestsPercent = artisticInterestsPercent;
	}

	public int getEmotionalityPercent() {
		return emotionalityPercent;
	}

	public void setEmotionalityPercent(int emotionalityPercent) {
		this.emotionalityPercent = emotionalityPercent;
	}

	public int getImaginationPercent() {
		return imaginationPercent;
	}

	public void setImaginationPercent(int imaginationPercent) {
		this.imaginationPercent = imaginationPercent;
	}

	public int getIntellectPercent() {
		return intellectPercent;
	}

	public void setIntellectPercent(int intellectPercent) {
		this.intellectPercent = intellectPercent;
	}

	public int getAuthorityChallengingPercent() {
		return authorityChallengingPercent;
	}

	public void setAuthorityChallengingPercent(int authorityChallengingPercent) {
		this.authorityChallengingPercent = authorityChallengingPercent;
	}

}
